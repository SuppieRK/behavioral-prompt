def score_sample_harness_smoke(context):
    diff = context.require("diff")
    changed_files = context.require("changed_files")
    validation_ok = context.require("harness_validation.success_status")
    return [
        check("updates greeting implementation", "Hello, eval harness!" in diff, "greeting implementation updated", "greeting implementation was not updated"),
        check("changes expected file only", tuple(changed_files) == ("src/app.py",), "only src/app.py changed", f"unexpected changed files: {changed_files}"),
        check("validation passed", bool(validation_ok), "harness validation passed", "harness validation did not pass"),
    ]


score_sample_harness_smoke.stable_id = "evals.cases.evaluation_mechanics.score_sample_harness_smoke"
